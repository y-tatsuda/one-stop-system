-- =====================================================
-- 中古在庫テーブルにカラー（色）カラムを追加
-- =====================================================
-- 実行方法: SupabaseダッシュボードのSQL Editorで実行

-- カラーカラムを追加
ALTER TABLE t_used_inventory
ADD COLUMN IF NOT EXISTS color VARCHAR(10);

-- コメント追加
COMMENT ON COLUMN t_used_inventory.color IS 'iPhone本体色コード（BK/WH/GD/YL/BL/RD/GR/PK/PP/OR/CR/JBK/NT/BT/WT/DT）';

-- インデックス追加（検索用）
CREATE INDEX IF NOT EXISTS idx_used_inventory_color ON t_used_inventory(color);

-- =====================================================
-- カラーコード一覧（参考）
-- =====================================================
-- 基本カラー:
--   BK: ブラック系（ブラック/グラファイト/スペースグレイ/ミッドナイト/スペースブラック）
--   WH: ホワイト系（ホワイト/シルバー/スターライト/クラウドホワイト）
--   GD: ゴールド（ゴールド/ライトゴールド）
--   RD: レッド（(PRODUCT)RED）
--
-- ブルー系:
--   BL: ブルー系（ブルー/パシフィックブルー/シエラブルー/ディープブルー/スカイブルー）
--   BT: ブルーチタニウム（15 Pro専用）
--
-- その他カラー:
--   OR: オレンジ（コズミックオレンジ）※17 Pro専用
--   YL: イエロー
--   GR: グリーン系（グリーン/アルパイングリーン/ミントグリーン/ティール）
--   PK: ピンク ※7はローズゴールド
--   PP: パープル系（パープル/ディープパープル/ラベンダー）
--   CR: コーラル（XR専用）
--   JBK: ジェットブラック（7専用）
--
-- チタニウム系（15 Pro〜16 Pro）:
--   NT: ナチュラルチタニウム
--   WT: ホワイトチタニウム
--   DT: デザートチタニウム
--   ※17 Proはアルミボディのためチタニウム系なし
